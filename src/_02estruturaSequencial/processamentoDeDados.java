package _02estruturaSequencial;

public class processamentoDeDados {
    public static void main(String[] args) {
        // area do trapezio

        double b, B, h, area;

        b = 6.0;
        B = 8.0;
        h = 5.0;

        area = (b + B) / 2.0 * h;

        System.out.println(area); // = 35.0
    }
}
